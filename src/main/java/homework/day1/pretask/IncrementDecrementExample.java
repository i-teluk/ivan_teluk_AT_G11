package main.java.homework.day1.pretask;

public class IncrementDecrementExample {
    public static void main(String[] args) {
        int a = 2;
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(--a);
        System.out.println(a--);
        System.out.println(a);
    }
}
