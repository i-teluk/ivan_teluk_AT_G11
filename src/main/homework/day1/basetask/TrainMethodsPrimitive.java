package main.homework.day1.basetask;

public class TrainMethodsPrimitive {
    public void printInt(int a) {
        System.out.println("я получил на вход число " + a);
    }

    public void printLong(long b) {
        System.out.println("я получил на вход длинное число " + b);
    }

    public void printChar(char c) {
        System.out.println("я получил на вход символ " + c);
    }

    public void printFloat(float f) {
        System.out.println("я получил на вход дробное число " + f);
    }

    public void printDouble(double d) {
        System.out.println("я получил на вход длинное дробное число " + d);
    }

    public void printShort(short s) {
        System.out.println("я получил на вход короткое число " + s);
    }

    public void printByte(byte b) {
        System.out.println("я получил на вход очень короткое число " + b);
    }

    public void printBoolean(boolean b) {
        System.out.println("я получил на вход булево " + b);
    }
}
